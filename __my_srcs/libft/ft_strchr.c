#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (unsigned char)c)
			return ((char *)(s + i));
		i++;
	}
	if (i == ft_strlen(s) && (char)c != '\0')
		return (NULL);
	return ((char *)(s + i));
}
